                            <h2 class="title">Procedimientos Emergencia <small> Controladores </small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                   <p  align="justify">Cuando una aeronave se declara en emergencia, hay una serie de cosas que debemos hacer para que se desarrolle con normalidad en toda su duraci&oacute;n.</p>

									<p  align="justify">El controlador debe obtener informaci&oacute;n relevante sobre lo sucedido a la aeronave, debe consultar qu&eacute; tipo de falla tuvo, cu&aacute;ntas almas (personas) abordo lleva, cu&aacute;l es su autonom&iacute;a, si va a poder llegar a aterrizar en pista y por &uacute;ltimo, si necesita los servicios de emergencia.<br />
									Una vez que el controlador disponga de esos datos, va a poder manejar de una manera m&aacute;s efectiva la emergencia.</p>

									<p  align="justify">El controlador debe recordar que si se encuentran activas otras dependencias, tiene la obligaci&oacute;n de anunciar y coordinar con las dem&aacute;s dependencias la emergencia para que estas lo sepan y se lo comuniquen a las dem&aacute;s aeronaves, as&iacute; como tambi&eacute;n lo deber&aacute; hacer con las aeronaves que se encuentren en su frecuencia.</p>

									<p  align="justify">El controlador debe dejar en claro a todas las aeronaves que hay una emergencia y que por tal motivo el aeropuerto se encuentra cerrado hasta la finalizaci&oacute;n de la misma. &iquest;Qu&eacute; quiere decir esto? Que ninguna aeronave podr&aacute; despegar ni aterrizar hasta que la aeronave en emergencia declare que esta est&aacute; terminada.&nbsp; Esto no quiere decir que los vuelos que ya est&eacute;n en el aire deban volar a un alternativo, el controlador debe tener la capacidad para indicarles qu&eacute; hacer para evitar cualquier tipo de interferencia con la aeronave en emergencia. A continuaci&oacute;n veremos un ejemplo:</p>

									<p  align="justify">Una aeronave esta arribando a Mar del Plata y declara emergencia por la p&eacute;rdida de un motor. El controlador tiene que tener en claro que la prioridad la tiene esa aeronave, y por ende, si hay otras aeronaves en la trayectoria, debe acomodarlas de manera tal que la que est&aacute; en emergencia tenga prioridad en su aterrizaje. En el caso de que haya vuelos VFR cerca de la zona, les har&aacute; hacer esperas sobre alg&uacute;n punto para mantener la zona que circunda al aer&oacute;dromo libre de tr&aacute;ficos. Las aeronaves en tierra se podr&aacute;n movilizar sin problemas hasta el punto de espera, tambi&eacute;n si alguna necesita reabastecer combustible, lo puede hacer sin ning&uacute;n problema.</p>

									<p  align="justify">Durante una emergencia, el controlador debe comunicarse &uacute;nicamente con la aeronave afectada, podr&aacute; despachar a los tr&aacute;ficos que est&aacute;n ya en el aire y podr&aacute; comunicarse con los vuelos para indicarles lo que van a hacer para mantener el a&eacute;rea libre. Sin embargo, no puede autorizar ning&uacute;n plan de vuelo bajo ninguna circunstancia. Debe mantener la frecuencia en silencio y solo atender a la aeronave en emergencia ignorando los pedidos de los pilotos en tierra y aire para volver al aer&oacute;dromo, despegar o aterrizar.</p>

									<p  align="justify">El controlador debe brindarle a la emergencia, informaci&oacute;n meteorol&oacute;gica tales como el viento, la visibilidad, todo lo que respecta a las nubes y el QNH local. Tambi&eacute;n puede sugerirle qu&eacute; maniobras hacer para llegar de una manera m&aacute;s r&aacute;pida a la pista.</p>

                    
