          <p style="text-align:justify"> Bienvenidos/as a la <strong>Intraweb</strong> de la <strong>Divisi&oacute;n Argentina</strong>. En ella encontrarás toda información relevante a tu entrenamiento y al Departamento de Training de IVAO Argentina. En el siguiente vídeo podrás ver un tutorial básico que te ayudará en tu aprendizaje sobre cómo funciona la Secretaría Virtual.</p>
          
          <p>Te describimos algunas de las opciones que actualmente tiene disponible la Secretaría Virtual:</p>
          
          <ul>
          <li><strong>Expediente</strong>: Podrá consultar sus datos personales correspondientes a su formación así como los entrenamientos actualmente disponibles acorde a su rango. </li>
          <li><strong>Trainings</strong>: Encontrará un listado con los trainings que ha realizado en la División Argentina así como información útil al respecto.</li>
          <li><strong>Exámenes</strong>: Dispondrá de los exámenes cursados en la División Argentina además de las calificaciones y datos relativos a los mismos.</li>
          <li><strong>Calificaciones</strong>: Podrá consultar las calificaciones obtenidas en los cursos que haya realizado en el Campus Virtual.</li>
          <li><strong>GCA</strong>: Encontrará la información relativa al Guest Controller Approval que haya solicitado.</li>
          <li><strong>Cursos</strong>: Tendrá a disposición la ofera de cursos actual en el Campus Virtual así como los Trainings.</li>
          <li><strong>Trámites</strong>: Tendrá disponibles todos los trámites que le corresponden como usuario.</li>
          <li><strong>Piloto Certificado</strong>: Si es usted miembro del AR Pilots Group, encontrará un listado con todas las participaciones que ha realizado como Piloto Certificado.</li>
          
          </ul>
          
          
