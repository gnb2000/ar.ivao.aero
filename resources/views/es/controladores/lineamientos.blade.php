                            <h2 class="title">Coordinación <small> Controladores </small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                   <p align="justify">Es un servicio que tienen como fin ordenar, agilizar, separar y hacer seguro el flujo de tránsito aéreo. Lo podemos clasificar en tres sub-servicios:</p>
                                   
                                   <p  align="justify"></p>
                                   <p  align="justify"></p>
                                   
                                   <h3>Canales</h3>
                                   
                                   <p align="justify">Se hará exclusivamente por canales privados entre los controladores. Puede utilizarse el chat privados del IvAc o como el INTERCOM en conjunción con Skype, indistintamente.</p>
                                   
                                   <p  align="justify"></p>
                                   <p  align="justify"></p>
                                   
                                   <h3>Idiomas</h3>
                                   
                                   <p align="justify"> El idioma a utilizar es el castellano entre dependencias de nuestro país y dependencias lindantes de países limítrofes como las de Chile, Uruguay, Bolivia y Paraguay, y en inglés o portugués con las de Brasil o exclusivamente en inglés con otras dependencias de otros países.</p>
                                   
                                   <p  align="justify"></p>
                                   <p  align="justify"></p>
                                   
                                   <h3>Procedimientos</h3>
                                   
                                   <p align="justify"> La coordinación debe ser de común acuerdo acorde a las necesidades de tránsito y del vuelo en cuestión. Siempre prevalecerá sobre el acuerdo la decisión tomada por el controlador que recibirá la aeronave.
Por ejemplo; un vuelo que está arribando al aeropuerto de Córdoba (SACO) debe ser transferido por Córdoba Control (SACO_APP) a Córdoba Torre (SACO_TWR) en un punto dentro de los límites del espacio aéreo controlado por Córdoba Torre, es decir el CTR Córdoba o bien unas millas antes.</p>
                                   
                                   <p align="justify">SACO_TWR solicitará a SACO_APP en la coordinación donde quiere que esté le transfiera la aeronave y con qué parámetros de vuelo (nivel y velocidad). Dado el caso que el control superior solicite información de cartas en uso, este deberá informarle al piloto que para mayor información contacte con la otra dependencia.
Dado el caso al revés, que una aeronave sale, la coordinación debe hacerse previa al despegue brindando restricciones de ascenso o solicitudes de cruzar un punto por encima o por debajo de cierto nivel.</p>
                                   
                                   
                    
