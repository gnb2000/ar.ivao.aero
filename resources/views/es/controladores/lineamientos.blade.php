                            <h2 class="title">Espacios Aéreos <small> Controladores </small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                    <p  align="justify">El espacio aéreo es una masa de aire de dimensiones definidas vertical y lateralmente. Encontraremos en la Argentina los siguientes:</p>
                                    
                                    <h3>FIR espacio aéreo superior <small>Región de Información de vuelo</small></h3>
                                    <p  align="justify">Espacio aéreo de dimensiones definidas que se extiende hacia arriba, en forma ilimitada, desde límite vertical superior de una o más regiones de información de vuelo (FIR). En dicho espacio aéreo se facilitan los servicios de información de vuelo y alerta.</p>
                                    
                                    <h3>FIR espacio aéreo inferior <small>Región de Información de vuelo</small></h3>
                                    <p  align="justify">Espacio aéreo de dimensiones definidas, dentro del cual se facilitan los servicios de información de vuelo y de alerta.</p>
                                    <h3>CTA <small>Área de Control</small></h3>
                                    <p  align="justify">Espacio aéreo controlado que se extiende hacia arriba desde un límite especificado sobre la superficie terrestre. Las áreas de control pueden ser:</p>
                                    
                                    	<ul>
                                        	<li><strong>AWY</strong>: Área de control o parte de ella dispuesta en formar de corredor y equipada con radioayudas para la navegación.</li>
                                            <li><strong>TMA</strong>: Área de control establecida generalmente en la confluencia de rutas ATS en las inmediaciones de uno o más aeródromos principales.</li>
                                        </ul>
                                    
                                    <h3>CTR <small>Zona de Control</small></h3>
                                    <p  align="justify"> Espacio aéreo controlado que se extiende hacia arriba desde la superficie terrestre hasta un límite superior especificado. Se determina en las proximidades de aeródromo con vuelos IFR y puede incluir más de un aeródromo dentro de si.</p>
                                    
                                    <h3>ATZ <small>Zona de Tránsito Aéreo</small></h3>
                                    <p  align="justify"> Espacio aéreo de dimensiones definidas establecidas alrededor de un aeródromo para la protección de tránsito de aeródromo.</p>
                            
                            

