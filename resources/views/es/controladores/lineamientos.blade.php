Por ello con el objeto de simular la realidad se definirán a continuación qué posiciones dan servicio de control radar. Estas están facultadas a dar vectores siempre por encima de la MRVA de cada sector, expresadas en la <a href="http://ais.anac.gov.ar/descarga/aip-5209704d8fffd" target="ext" class="text-info">AIP ENR 1.6</a> y no hay necesidad de solicitarle al piloto que reporte constantemente su posición ya que está en la pantalla radar.</p>

<p align="justify">Todas las demás dependencias hacen control por procedimientos, es decir; no pueden dar vectores, solo referencias radioeléctricas o magnéticas y tienen que pedirle al piloto que reporte su posición y parámetros de vuelo.</p>

