                            <h2 class="title">FRAs <small> Asignación de Rangos a Dependencias </small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                   <p align="justify">El criterio de asignación de rangos se realizó con restricción a las posiciones de torre de control (_TWR) que abarquen un CTR y TMA conjuntamente debido a la potencial carga de trabajo producto de la gran cantidad de espacio aéreo a controlar y los servicios de control que deban brindarse, asignándose el rango de AS3.</p>
                                   
                                   <p align="justify">De igual manera las aproximaciones (_APP) de abarquen únicamente un TMA. Sin embargo las posiciones de control de aproximación que abarquen un TMA y brinden control radar serán AS3 + Inglés.
Y resultaran ADC aquellas en donde el TMA tenga una base escalonada (Baires Control y Resistencia Control)</p>

									<p align="justify">Por otra parte las torre de control (_TWR) que abarque solo un CTR tendrán la asignación de AS2. Las posiciones de control de superficie (_GND) y autorizaciones (_DEL) serán AS1 o AS2 indistintamente.
Las posiciones de control de área (_CTR) serán ADC mínimo debido al requerimiento linguístico y al gran área de cobertura de las mismas.</p>

									<p align="justify"><strong>Excepciones</strong>: En caso de eventos, exámenes u otra situación que el Departamento de Operaciones de ATC considere, se liberará las FRA a algunos miembros en particular. El AR-AOC se reserva este derecho.</p>

                    
