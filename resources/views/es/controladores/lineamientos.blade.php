                            <h2 class="title">Lineamientos <small> Controladores </small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                   <p  align="justify">Es un servicio que tienen como fin ordenar, agilizar, separar y hacer seguro el flujo de tránsito aéreo. Lo podemos clasificar en tres sub-servicios:</p>
                                   
                                   <ul>
                                   	<li>Servicio de control de aeródromo: Suministrado al tránsito de aeródromo. La dependencia de control de tránsito aéreo que lo brinda es la TWR (Tower /Torre de control de aeródromo) en los ATZ y CTR.</li>
                                    <li>Servicio de control de aproximación: Suministrado para la llegada y salida de vuelos controlados. Es brindado por la APP (Approach / Oficina de Control de aproximación) en los CTR y TMA.</li>
                                    <li>Servicio de control de área: Suministrado a los vuelos controlados en áreas de control. Es brindado por el ACC (Area Control Center / Centro de Control de Área) en las CTA, TMA y AWY.</li>
                                   </ul>
                                   
                                   <p  align="justify">En muchas ocasiones, en nuestro país no es necesario poner una dependencia por cada sub-servicio de control, por lo cual una sola dependencia puede dar uno, dos, e inclusive, tres servicios simultáneamente y controlar uno o más espacios aéreos.
Es decir que Rosario Torre (SAAR_TWR) hace el trabajo de torre de control y control de aproximación ya que controla todo el CTR y TMA. Por eso mismo también hace trabajo de Centro cuando las aeronaves cruzan su TMA volando en ruta.</p>
                    
