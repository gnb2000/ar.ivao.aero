                                        <p align="justify">Este permiso debe coordinarse con la/las dependencias superiores a los efecto de planificar en conjunto la salida de la aeronave en cuestión para no interferir con la trayectoria de otras aeronaves en llegada, salida o ruta. El controlador correspondiente verificará que el piloto complete con A (para altitudes) cuando el vuelo se desarrolle por debajo del TA (altitud de transición) y F (para niveles de vuelo) cuando este se desarrolle por encima de la TA. Ningún vuelo podrá desarrollarse en la TA o en el TRL ni en el espacio que existiera entre estos niveles. Por nivel entendemos la distancia vertical de una aeronave indistintamente de la referencia que se tome, a saber; altura, altitud o nivel de vuelo.</p>
