                            <h2 class="title">GND <small> Control de Superficie</small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
									<p align="justify">El control de superficie, GND acrónimo de ground en inglés, tiene jurisdicción sobre las aeronaves que estén en tierra, se estén moviendo o no, excepto la pista de despegue/aterrizaje que es pura y exclusiva jurisdicción de la TWR (Torre de control de aeródromo).

									<p align="justify">Superficie dará regulará los movimiento en tierra, tales como el rodaje de pista a plataforma, plataforma a pista, plataforma a plataforma, etc. Siempre que existiere la necesidad de cruzar la pista, la aeronave deberá aguardar fuera de esta hasta que Superficie coordine con Torre el cruce de la misma. También dará autorizaciones de retroceso y puesta en marcha y el movimiento de vehículos terrestres.</p>

									<p align="justify">No todos los aeropuertos cuentan con servicio de control de superficie, así como no todos cuentan con autorizaciones (DEL). En este caso GND hará las de DEL brindando en primera comunicación el permiso de tránsito.</p>

									<p  align="justify">¿Qué necesito para controlar superficie?</p>
									<ul>
										<li>Plano de aeródromo (disponible en la sección de cartografía de la web)</li>
										<li>Archivo de sector con el aeropuerto en cuestión.</li>
									</ul>
									<p align="justify">En el primer contacto debemos chequear que el piloto confirme que tiene la información ATIS abordo. De esa manera ya sabemos que tiene en consideración las condiciones meteorológicas, pistas en uso, etc. Si no la tiene debe comunicársela.</p>

									<p align="justify">Luego se requerirá el permiso de tránsito (ya explicado en Autorizaciones / DEL) y luego el retroceso o remolque (pushback) y puesta en marcha. En ese caso se dará el turno (1, 2, 3, etc.) dependiendo si hay otras aeronaves en el área de movimiento, indicando la pista en uso y QNH.</p>

									<p align="justify">Finalizado se autorizará el rodaje vía las calles de rodaje necesarias hasta el punto de espera de la pista en cuestión. Si existiere el caso de una aeronave deba rodar de la otra se hará hincapié en ello, a modo de ejemplo se dará la siguiente autorización:</p>
									
									<p align="justify"><em>LV-ARG autorizado rodaje detrás de un B738 a punto de espera de 13 via A y 5, detrás del B738.</em></p>
									
									<p align="justify"> Como se puede apreciar, debe remarcarse que debe ir detrás del B738 (Bravo Siete Tres Ocho).</p>

									<p align="justify">En caso de trafico convergente o aproximaciones de frente en tierra. Quedará a criterio del ATC quién cederá el paso a quién.</p>

									<p align="justify">Una vez la aeronave se encuentra en punto de espera se la transferirá a la depende de torre (TWR) haciendo clic botón derecho sobre la aeronave y tocando el botón TRANSFER se seleccionará el ATC al cual transferirlo. También debe informársele al piloto de la transferencia, por ejemplo:</p>
									
									<p align="justify"><em>LV-ARG contacte Aeroparque Torre en 118.85, hasta la vuelta.</em></p>

									<p align="justify">De igual manera, el transito arribando, una vez evacuada la pista debe transferirse a superficie.</p>

									<p align="justify">Si no hubiera un controlador superior adyacente debe tocar RELEASE así pasar a la aeronave a UNICOM en 122.800 mHz.</p>

									<p align="justify">Para acceder a todas estas interacciones de transferencia y liberación debe asumirse siempre al transito a través del botón ASSUME en el menú que sale cliqueando con botón derecho sobre la aeronave.</p>

									<p align="justify">La aeronave que libera la pista en uso se le autorizará el rodaje a plataforma, una vez en la plataforma se lo autorizará a liberar frecuencia, por ejemplo:</p>
									<p align="justify"><em>LV-ARG autorizado a liberar frecuencia, hasta la vuelta. Muchas gracias por volar en IVAO Argentina.</em></p>

                    
                    </div><!-- tabpanel -->
                    
                    <div role="tabpanel" class="tab-pane" id="twr">
                            
                            <h2 class="title">TWR <small> Torre de Control de Aerodromo</small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                   <p  align="justify">El controlador de torre (TWR) tiene como &aacute;rea de jurisdicci&oacute;n la Zona de Control (CTR) que circunda al aer&oacute;dromo y la o las pistas que hubieran en el mismo.</p>

									<p  align="justify">Se encarga de autorizar despegues, aterrizajes, circuitos de tr&aacute;nsito, aplicar procedimientos de aproximaci&oacute;n instrumental mediante el uso de IAC (<em>Instrument Approach Charts</em>), cruces de pista, y todos los movimientos dentro de su CTR o el espacio a&eacute;reo de jurisdicci&oacute;n el cual se le asigne.</p>

									<p  align="justify">En la mayor&iacute;a de los aeropuertos de la Rep&uacute;blica Argentina las torre controlan las Zonas de Control (<strong>CTR</strong>) y las &aacute;reas de Control Terminal (<strong>TMA</strong>) haciendo el trabajo conjunto de TWR y APP en la misma frecuencia el mismo controlador. De ser este caso, la TWR tambi&eacute;n asignar&aacute; salidas y llegadas normalizadas (SID/STAR) a los fijos de salida o llegada del TMA de jurisdicci&oacute;n.</p>

									<p  align="justify">Los &uacute;nicos aeropuertos que cuentan con APP son SAEZ y SABE (a trav&eacute;s de SABA_APP), SACO, SARE, SAME, SAZM, SAZB, SAVC y SAWG.</p>

									<p  align="justify">&iquest;Qu&eacute; necesito para controlar Torre?</p>

									<ul>
										<li>Plano de aer&oacute;dromo</li>
										<li>Cartas de Aproximaci&oacute;n Instrumental (IAC)</li>
										<li>Cartas de salidas y llegadas normalizadas (SID/STAR)</li>
										<li>Archivo de sector del aeropuerto</li>
										<li>Documento AD 2.0 del aeropuerto (disponible en <a href="http://ais.anac.gov.ar/aip">http://ais.anac.gov.ar/aip</a> apartado &quot;Ad&quot;)</li>
									</ul>

									<p  align="justify">Es plenamente mandatorio conocer el espacio a&eacute;reo a controlar, tanto CTR como TMA si fuera el caso de controlar antes, las pistas, cartas y aproximaciones en uso, salidas y llegadas normalizadas, aerov&iacute;as inferiores y superiores que cruzan nuestro espacio a&eacute;reo, as&iacute; como los limites horizontales y verticales de los mismos.</p>

									<p  align="justify">Las autorizaciones de despegue-aterrizaje deben otorgarse junto a la informaci&oacute;n de vientos en direcci&oacute;n e intensidad. La direcci&oacute;n debe darse redondeada, esto quiere decir que vientos de los 356/13 deben informarse como vientos de los 360&deg; a 13 nudos. Cuando la intensidad de los vientos sea menor a 5 nudos podr&aacute; elegirse la pista en uno que cuente con mejores condiciones para la aproximaci&oacute;n como radioayudas, indicaciones lum&iacute;&shy;nicas, etc. De igual forma toda autorizaci&oacute;n de descenso por debajo del nivel de transici&oacute;n debe otorgarse el QNH local en hectopascales.</p>

									<p  align="justify">Asimismo es trabajo de la torre determinar el nivel de transici&oacute;n (ver Manual de Altimetr&iacute;a en la Biblioteca).</p>

									<p  align="justify">Siempre se debe coordinar las salidas con APP, en el caso de que no hubiera, o sea un TWR/APP directamente informar a Centro la transferencia de la aeronave en el fijo de salida del TMA as&iacute;&shy; como el horario de arriba a dicho putno y el nivel de vuelo que la aeronave prevee alcanzar.</p>

                    
                    </div><!-- tabpanel -->
                    
                    <div role="tabpanel" class="tab-pane" id="app">
                            
                            <h2 class="title">APP <small>Oficina de Control de Aproximación </small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                   <p  align="justify">En la Rep&uacute;blica Argentina el control de aproximaci&oacute;n se encuentra separado al controlador de torre en los siguientes aeropuertos:</p>

									<ul>
										<li>SABE y SAEZ, proporcionado por Baires Control (SABA_APP) *</li>
										<li>SAZM, proporcionado por Mar del Plata Control (SAZM_APP) *</li>
										<li>SARE, proporcionado por Resistencia Control (SARE_APP)</li>
										<li>SACO, proporcionado por C&oacute;rdoba Control (SACO_APP) *</li>
										<li>SAME, proporcionado por Mendoza Control (SAME_APP) *</li>
										<li>SAWG, proporcionado por Gallegos Control (SAWG_APP)</li>
										<li>SAVC, proporcionado por Comodoro Control (SAVC_APP)</li>
										<li>SAZB, proporcionado por Bah&iacute;a Blanca Aproximaci&oacute;n (SAZB_APP)</li>
									</ul>

									<p  align="justify">De estas dependencias aquellas indicadas con un asterisco (*) brindar servicio de control radar.</p>

									<p  align="justify">Nota: Si bien el c&oacute;digo de conexi&oacute;n es con la extensi&oacute;n _APP, todas deben llamarse por radio &quot;Control&quot; excepto Bah&iacute;a Blanca ya que as&iacute; lo determina la AIP.</p>

									<p  align="justify">Estas se limitan a controlar &uacute;nicamente el TMA, llevando a las aeronave en llegada desde el fijo de ingreso al TMA hasta el punto en el que inician un procedimiento IAC, de forma directa o a trav&eacute;s de una llegada normalizada (STAR), tambi&eacute;n se encarga de las aeronaves que realizan esperas en alg&uacute;n punto bajo el control de esta dependencia. De manera inversa para las aeronaves en salida, esta dependencia gu&iacute;a a la aeronave desde el ascenso inicial posterior al despegue (normalmente 3000 pies sobre el terreno) hasta el punto de salida, de forma directa o a trav&eacute;s de una salida normalizada (SID).</p>

									<p  align="justify">&iquest;Qu&eacute; necesito para controlar APP?</p>

									<ul>
										<li>Cartas de Aproximaci&oacute;n Instrumental (IAC)</li>
										<li>Cartas de salidas y llegadas normalizadas (SID/STAR)</li>
										<li>Archivo de sector del aeropuerto</li>
										<li>Documento AD 2.0 del aeropuerto (disponible en <a href="http://ais.anac.gov.ar/aip">http://ais.anac.gov.ar/aip</a> apartado &quot;Ad&quot;)</li>
										<li>Carta de Gu&iacute;&shy;a Vectorial Radar (<a href="http://ais.anac.gov.ar/descarga/aip-5209704d8fffd">http://ais.anac.gov.ar/descarga/aip-5209704d8fffd</a>) &acirc; SOLO PARA CONTROL RADAR</li>
									</ul>

									<p  align="justify">Nunca debe darse vectores por debajo de las altitudes o FL m&iacute;&shy;nimos.</p>

									<p  align="justify">Las transferencias de APP a TWR deben realizarse 10 millas antes de los l&iacute;&shy;mites del CTR o punto de traspaso para iniciar la aproximaci&oacute;n. De TWR a APP alcanzados 3000 pies sobre el terreno.</p>

									<p  align="justify">Las transferencias de APP a CTR (Centro) se realizan alcanzando el fijo de salida del TMA o antes de alcanzar el limite vertical de este, usualmente FL245 de manera inversa es de CTR a APP.</p>

									<p  align="justify">En ciertos casos que hubieran dos TMA contiguos la transferencia debe realizarse entre ambas dependencias omitiendo a CTR. Si es el caso, por ejemplo, de los TMA La Rioja (SANL), Catamarca (SANC), Tucuman (SAN), Santiago del Estero (SANE) y Salta (SASA) las transferencias deben realizarse entre estas TWR (ya que no tienen APP estos aeropuertos, y la TWR hace de TWR/APP controlando tambi&eacute;n el TMA) omitiendo a C&oacute;rdoba Centro (SACF_CTR).</p>

									<p  align="justify">Si no hubiera una torre conectada y s&iacute;&shy; estuviera Centro, en ese caso en lugar de pasar la aeronave a UNICOM la transferimos a Centro.</p>

                    
                    </div><!-- tabpanel -->
                    
                    <div role="tabpanel" class="tab-pane" id="ctr">
                            
                            <h2 class="title">CTR <small> Centro de Control de Área (ACC)</small><span class="line"></span></h2>
                            
                            		<div class="separacion-tablas"></div>
                                    
                                   <p  align="justify">El servicio de control de &aacute;rea es brindado por el ACC (Area Control Center / Centro de Control de &Aacute;rea) o mayormente conocido como CTR en IVAO por su acrononimo en ingles de CenTeR. Cabe aclarar que CTR referido a la extensi&oacute;n _CTR de un Centro no es lo mismo que CTR referido a Zona de Control como espacio a&eacute;reo.</p>

									<p  align="justify">El controlador de centro se tiene como jurisdicci&oacute;n todas las aerov&iacute;&shy;as (AWY / airway) dentro de un FIR. Eso quiere decir que no controla cualquier aeronave dentro del FIR, sino que controla cualquier aeronave en cualquier AWY dentro de su FIR. Para eso debe darse la condici&oacute;n que la aeronave est&eacute; dentro de los limites verticales y horizontales de la aerov&iacute;a.</p>

									<p  align="justify">Las aeronaves en ruta deben mantener una separaci&oacute;n de 15nm y 1000 entre el nivel minimo de aerov&iacute;&shy;a y FL290, y 1000 o 2000 pies entre FL290 y FL410 (dependiendo si la aeronave est&aacute; certificada RVSM / Reduced Vertical Separation Minima / Separaci&oacute;n Vertical Minima Reducida)</p>

									<p  align="justify">En materia de transferencias recordemos que la mayor parte de los TMA que son controlados &iacute;ntegramente por las TWR, exceptuando los casos que son controlados TMA y CTR por APP y TWR respectivamente. Es decir que transferencias de CTR a TWR son totalmente usuales en muchos aeropuertos argentinos. Las transferencias deben hacerse en coordinaci&oacute;n con el controlador adyacente, esto quiere decir que vamos a autorizar descensos en base a lo coordinador, por ejemplo:</p>

									<p  align="justify"><em>LV-ARG llame listo para descenso, prevea alcanzar ASADA con FL110</em></p>

									<p  align="justify">De igual manera debe hacerse en ascensos.</p>

									<p  align="justify">Tengamos en consideraci&oacute;n que muchos TMA tienen como l&iacute;mite FL245, otros tienen l&iacute;&shy;mites m&aacute;s bajo como FL195, FL075, etc. esto quiere decir que si una aeronave volando por una aerov&iacute;a a FL180 ingresa a un TMA de techo limite de FL245, CTR debe pasar al tr&aacute;nsito con la TWR o APP de jurisdicci&oacute;n mientras que la aeronave vuela la aerov&iacute;&shy;a dentro de ese TMA. Si el techo l&iacute;&shy;mite del TMA hubiera sido FL075 la aeronave hubiera volado la ruta &iacute;&shy;ntegramente con CTR.</p>

									<p  align="justify">Las transferencias entre distintos CTR deben realizarse 15 nm antes del punto l&iacute;&shy;mite entre ambas FIR previa coordinaci&oacute;n.</p>

									<p  align="justify">Para brindar separaci&oacute;n deben utilizarse reducciones/aumentos de velocidad o bien traspasos vectoriales. Podemos apreciar m&aacute;s t&eacute;cnicas de separaci&oacute;n en el Manual B&aacute;sico ATC en la Biblioteca de Entrenamiento de IVAO Argentina en nuestro sitio web.</p>

									<p  align="justify">Centro es encargado de brindar los c&oacute;digos de respondedor en caso que el generador autom&aacute;tico (TCG) no funcione.</p>

									<p  align="justify">&iquest;Qu&eacute; necesito para controlar CTR?</p>

									<ul>
										<li>Cartas de navegaci&oacute;n en ruta (ENR) inferior y superior.</li>
										<li>Conocimiento de las aerov&iacute;as y TMA dentro del FIR.</li>
										<li>Conocer los procedimientos locales de cada aeropuerto dentro del FIR.</li>
										<li>Conocer las sub-divisiones del espacio a&eacute;reo dentro del FIR.</li>
										<li>Conocer los procedimientos de control del FIR.</li>
									</ul>

									<p  align="justify">Es importante resaltar que el &uacute;nico CTR que brinda servicio radar es Ezeiza Centro (SAEF_CTR) dentro del CTA Ezeiza (&Aacute;rea de Control Ezeiza). Este espacio a&eacute;reo es delimitado por un circulo de 100 nm con centro el el VOR EZE desde FL095 hasta FL450, y de 150 nm con centro en el VOR EZE desde FL145 hasta FL450. M&aacute;s all&aacute; de este espacio a&eacute;reo no se brinda servicio radar y debe solicitarse informe de posici&oacute;n y estimas a las aeronaves, los informes de posici&oacute;n deben ser otorgados de la siguiente manera:</p>

									<ul>
										<li>Identifcaci&oacute;n de la aeronave</li>
										<li>Posici&oacute;n</li>
										<li>Hora UTC</li>
										<li>Nivel de vuelo o altitud</li>
										<li>Pr&oacute;ximo punto y estima de arribo al mismo</li>
										<li>Pr&oacute;ximo punto al estimado</li>
									</ul>

									<p  align="justify">Por ejemplo:</p>

									<p  align="justify"><em>Ezeiza Centro, LV-ARG posici&oacute;n EGOKO a las 12:35 nivel de vuelo 220. Estimamos EDRON a las 12:53. Sigue NEU.</em></p>

									<p  align="justify">Y la respuesta de Ezeiza Centro ser&iacute;a:</p>

									<p  align="justify"><em>Recibido LV-ARG lo espero a las 12:53 sobre EDRON</em></p>

									<p  align="justify">Siempre que la estima al pr&oacute;ximo punto sea mayor a 30 minutos, la dependencia de Centro deber&aacute; solicitar reporte de &quot;vuelo normal&quot; cada 30 minutos. Supongamos que cruzamos EGOKO a las 12:35 y vamos a cruzar EDRON a las 13:15 vamos a solicitar reportar vuelo normal las 13:05 y EDRON 13:15.</p>

									<p  align="justify">Debe tenerse en consideraci&oacute;n que el ATIS de una posici&oacute;n CTR no debe llevar bajo ning&uacute;n punto pistas en uso, niveles y altitudes de transici&oacute;n ni informaci&oacute;n respecto a ning&uacute;n aeropuerto en particular. &Uacute;nicamente el nombre de la posici&oacute;n en ingl&eacute;s (EZEIZA CENTER, MENDOZA CENTER, etc.) y si fuera necesarios alg&uacute;n comentario en la secci&oacute;n &quot;Remarks&quot; como por ejemplo en el caso de SAEF:</p>

									<p  align="justify"><em>Radar services is provided within 100 and 150 nm within SAEZ over FL095 and FL145 respectively / Servicio radar provisto dentro de las 100 y 150 nm con centro en SAEZ a partide FL095 y FL145 respectivamente.</em></p>

                    
